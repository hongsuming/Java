
public class HelloTest { // 클래스 시작, 클래스 식별자(이름) : 파일 이름과 동일
						//  대문자, 영어로 시작
						// 클래스 안에 멤버변수, 생성자, 함수 3가지 선언 / 사용 가능

	public static void main(String[] args) { // main 함수 시작
		// 프로젝트 프로그램의 동작 시작점
		System.out.println("Hello"); // println 함수 : 문자열을 매개변수로
									// 받아서 콘솔 창에 출력 후 개행(줄 바꿈) 해주는 함수
		System.out.println("World");
	} // main 함수 끝 -> 프로그램 종료

} // 클래스 끝

